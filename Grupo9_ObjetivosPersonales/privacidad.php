                    y tener en cuenta las tareas más básicas. Para ver el
                    panorama completo, es una buena idea incluir tanto las
                    tareas personales como las laborales en una sola lista.
                    Todo, desde recoger la ropa de la tintorería hasta programar
                    una reunión personal con tu jefe, debe estar registrado en
                    el mismo lugar. Una vez que todo está escrito, se suele
                    hacer la lista de prioridades según la importancia,
                    urgencia, duración y recompensa de cada tarea.</br>
