                      lleguen trabajos nuevos y las prioridades cambien. Si
                      tienes una tarea asignada sin fechas claras de inicio o de
                      entrega, asegúrate de pedir esa información para poder
                      programar el tiempo que le dedicarás y plantear las
                      expectativas. </br> Una vez que conozcas esas fechas, incluso
