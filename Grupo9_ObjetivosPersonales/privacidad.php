                      complejas. Independientemente de que una tarea se haga en
                      minutos o te lleve una semana, siempre tiene un principio
                      y un fin. Así que, una vez que tengas tu lista, agrega una
                      fecha de inicio y una de entrega a cada tarea. </br> Al hacerlo
